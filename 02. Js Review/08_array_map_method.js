// The Array methods .map(), .filter(), .reduce() are called functional array methods as they do not mutate the original array instead return a new array based on original one

// Getting all books from the Data
const books = getBooks();
books;

// 1. Map Method:
// In JavaScript .map() method used to create a new array by applying a provided function to each element of an existing array
// Map expects a callback function inside of it for which each of the element will be called and processed
// It does not modify the original array but returns a new one

// Storing the 2X values in 'x' as an Array
const x = [1, 2, 3, 4, 5].map((el) => el * 2);
console.log(x);

// Storing the titles of all Books in 'titles' Array
const titles = books.map((book) => book.title);
console.log(titles);

// Storing only the essential info like title & author in 'essentialData' Array
// To return an Object without return in arrow function we need to enclose it in () parenthesis
const essentialData = books.map((book) => ({
  title: book.title,
  author: book.author,
  reviewCount: getTotalReviewCount(book),
}));
console.log(essentialData);
