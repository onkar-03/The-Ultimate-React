// 3. Reduce Method
// It is the most powerful & most versatile method of Arrays in Javascript
// The .reduce() method in JavaScript is used to execute a reducer function on each element of the array, resulting in a single output value
// This method is useful for accumulating values, such as summing numbers, creating a cumulative result, or transforming data into a different structure
// Reduce expects a callback function inside of it for which each of the element will be called and processed, it also takes in a starter value too such as an accumulator

// Syntax: .reduce((accumulator, otherParameter), startValueOfAccumulator)

// Calculating the Total Number of Pages in all Books, acc start value as 0
const pageAllBooks = books.reduce((acc, book) => acc + book.pages, 0);
console.log(pageAllBooks);

